// Shared footer for all dashboard pages.
// Injected at runtime so footer content is maintained in one place.
(function () {
  'use strict';

  // Determine path prefix from the script src: "footer.js" means root, "../footer.js" means one level down.
  var scripts = document.getElementsByTagName('script');
  var src = scripts[scripts.length - 1].getAttribute('src') || '';
  var prefix = src.indexOf('../') === 0 ? '../' : '';

  var footer = document.createElement('footer');
  footer.style.cssText = 'margin-top:2em;padding-top:1em;border-top:1px solid var(--border,#30363d);font-size:0.8em;color:#8b949e;';
  footer.innerHTML =
    'Generated by <a href="https://github.com/danmoseley/pr-dashboard">pr-dashboard</a> via GitHub Actions.' +
    ' <a href="https://danmoseley.github.io/repo-health-metrics/" style="margin-left:1em">&#x1F4CA; Repo Health</a>' +
    ' <a href="' + prefix + 'changelog.html" style="margin-left:1em">&#x1F4DD; Changelog</a>' +
    ' <a href="https://github.com/danmoseley/pr-dashboard/actions/workflows/generate-reports.yml" style="margin-left:1em">' +
    '<img src="https://github.com/danmoseley/pr-dashboard/actions/workflows/generate-reports.yml/badge.svg" alt="Pipeline status" style="vertical-align:middle"></a>';

  document.body.appendChild(footer);
})();
